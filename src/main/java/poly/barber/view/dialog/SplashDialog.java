void runLoading() {
    new javax.swing.Timer(20, e -> {
        int value = jProgressBar1.getValue();

        if (value < 100) {
            int newValue = value + 2;
            jProgressBar1.setValue(newValue);
            jProgressBar1.setString("Đang tải... " + newValue + "%");
        } else {
            ((javax.swing.Timer) e.getSource()).stop();
            dispose();

           new LoginDialog((poly.barber.view.Barber5AEJFrame) parentFrame).setVisible(true);
        }
    }).start();
}

