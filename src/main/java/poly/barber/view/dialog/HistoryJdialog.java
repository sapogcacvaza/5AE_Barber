    private void loadInvoiceDetail(String invoiceID) {
        int id = Integer.parseInt(invoiceID);
        Invoice invoice = invoiceRepo.getOne(id);

        if (invoice != null) {
            txtMaHoaDon.setText(String.valueOf(invoice.getInvoiceCode()));

            //Lấy appointmentID từ invoice
            int appointmentID = invoice.getAppointmentID();
            Appointment app = appointmentRepo.getOne(appointmentID);

            if (app != null) {
                int khachHangId = app.getCustomerID();
                Customer kh = customerRepo.getOne(khachHangId);
                if (kh != null) {
                    txtTenKH.setText(kh.getFullname());
                    txtSDT.setText(kh.getPhone());
                } else {
                    txtTenKH.setText("Không có thông tin");
                    txtSDT.setText("Không có thông tin");
                }
            }

            int nvID = invoice.getCreatedByEmployeeID();
            Employee emp = employeeRepo.getOne(nvID);
            if (emp != null) {
                txtNhanVien.setText(emp.getFirstname() + " " + emp.getLastname());
            } else {
                txtNhanVien.setText("Không xác định !");
            }

            // 1. Khai báo định dạng (Dấu phẩy phân cách, không lấy số lẻ thập phân)
            java.text.DecimalFormat df = new java.text.DecimalFormat("#,###");

            // 2. Ép kiểu dữ liệu về Number và format
            double amount = invoice.getTotalAmount().doubleValue();
            txtThanhTien.setText(df.format(amount)); // Kết quả sẽ là: 800,000
            txtTimeCheckIn.setText(String.valueOf(invoice.getCheckInDateTime()));
            txtTimeCheckOut.setText(String.valueOf(invoice.getCheckOutDateTime()));
            txtGiamGia.setText(String.valueOf(invoice.getTotalDiscount()));

            //txtPhuongThucThanhToan
            Payment p = paymentRepo.getOneByInvoiceID(invoiceID);
            if (p != null) {
                // Truy vấn nhanh cái tên dựa vào ID phương thức
                String sql = "SELECT PaymentName FROM PaymentMethod WHERE PaymentMethodID = ?";
                List<Object[]> list = XQuery.getRawList(sql, p.getPaymentMethodID());

                if (!list.isEmpty()) {
                    txtPhuongThucThanhToan.setText(list.get(0)[0].toString());
                }
            } else {
                txtPhuongThucThanhToan.setText("Chưa xác định");
            }

            int trangThai = invoice.getStatus();
            if (trangThai == 1) {
                rdoDaThanhToan.setSelected(true);
            } else {
                rdoDaHuy.setSelected(true);
            }
        }

        //txt chỉ xem
        txtMaHoaDon.setEditable(false);
        txtTenKH.setEditable(false);
        txtSDT.setEditable(false);
        txtThanhTien.setEditable(false);
        txtGiamGia.setEditable(false);
        txtNhanVien.setEditable(false);
        txtThanhTien.setEditable(false);
        txtTimeCheckIn.setEditable(false);
        txtTimeCheckOut.setEditable(false);
        txtPhuongThucThanhToan.setEditable(false);
    }

    private void fillServiceTable(String invoiceID) {
        DefaultTableModel model = (DefaultTableModel) tblDanhSachDV.getModel();
        model.setRowCount(0);

        List<Object[]> list = invoiceRepo.GetDetailsByInvoiceId(invoiceID);
        int stt = 1;
        for (Object[] row : list) {
            // row[0]: ServiceName (String)
            // row[1]: Quantity (Integer)
            // row[2]: Price (BigDecimal)
            // row[3]: Total (BigDecimal)

            // Cách ép kiểu an toàn cho cả BigDecimal và Double
            double price = ((Number) row[2]).doubleValue();
            double total = ((Number) row[3]).doubleValue();

            model.addRow(new Object[]{
                stt++,
                row[0],
                row[1],
                String.format("%,.0f", price), // Định dạng tiền 50,000
                String.format("%,.0f", total)
            });
        }
    }

